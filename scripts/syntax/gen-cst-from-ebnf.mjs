#!/usr/bin/env deno run

const FALLBACK_GRAMMAR = `program = rule.
rule = identifier.
identifier = "IDENT".
`;

const GRAMMAR_PATH = Deno.args[0] ?? "docs/clapse-language/references/grammar.ebnf";
const OUTPUT_PATH = Deno.args[1] ?? "lib/compiler/syntax_cst_generated.clapse";
const INPUT_GRAMMAR_PATH = Deno.args[0] ?? "docs/clapse-language/references/grammar.ebnf";
const FALLBACK_LABEL = "builtin-fallback";

if (import.meta.main) {
  main().catch((error) => {
    console.error(error);
    Deno.exit(1);
  });
}

async function main() {
  const sourcePath = await Deno.realPath(INPUT_GRAMMAR_PATH).catch(() => null);
  const grammarText = await readGrammar();
  const grammarRules = parseGrammar(grammarText);
  const sourceLabel = sourcePath ?? FALLBACK_LABEL;
  const generated = emitSyntaxClapse(grammarRules, sourceLabel);
  await Deno.writeTextFile(OUTPUT_PATH, generated);
}

function escapeClapseString(text) {
  return text
    .replaceAll("\\", "\\\\")
    .replaceAll('"', '\\"')
    .replaceAll("\n", "\\n")
    .replaceAll("\r", "\\r");
}

async function readGrammar() {
  try {
    return await Deno.readTextFile(GRAMMAR_PATH);
  } catch (error) {
    if (error instanceof Deno.errors.NotFound) {
      console.log(
        `info: grammar file missing at ${GRAMMAR_PATH}, using embedded fallback grammar`,
      );
      return FALLBACK_GRAMMAR;
    }
    throw error;
  }
}

function parseGrammar(text) {
  const entries = splitRulesFromText(text);
  const rules = entries.map(({ name, rhs }) => {
    const rhsText = rhs.replace(/[.;]\s*$/u, "").trim();
    const parser = new Parser(new Lexer(rhsText).tokenize());
    const expr = parser.parseExpr();
    if (!parser.eof()) {
      throw parser.error(`Trailing token when parsing rule ${name}`);
    }
    return { name, expr };
  });
  if (rules.length === 0) {
    return parseGrammar(FALLBACK_GRAMMAR);
  }
  return rules;
}

function splitRulesFromText(text) {
  const lines = text.split(/\r?\n/);
  const rules = [];
  let currentName = null;
  let currentRhs = [];
  for (const rawLine of lines) {
    const commentless = stripLineComment(rawLine);
    const line = commentless.trim();
    if (line.length === 0) {
      continue;
    }
    const headerMatch = /^([A-Za-z_][A-Za-z0-9_-]*)\s*=\s*(.*)$/u.exec(line);
    if (headerMatch) {
      if (currentName !== null) {
        rules.push({ name: currentName, rhs: currentRhs.join(" ") });
      }
      currentName = headerMatch[1];
      currentRhs = [headerMatch[2]];
      continue;
    }
    if (currentName !== null) {
      currentRhs.push(line);
    }
  }
  if (currentName !== null) {
    rules.push({ name: currentName, rhs: currentRhs.join(" ") });
  }
  return rules;
}

function stripLineComment(line) {
  const commentStart = line.indexOf("#");
  if (commentStart >= 0) {
    return line.slice(0, commentStart);
  }
  const dashed = line.indexOf("--");
  if (dashed >= 0) {
    return line.slice(0, dashed);
  }
  return line;
}

function emitSyntaxClapse(rules, sourceLabel) {
  const sortedRules = [...rules].map((rule, index) => ({
    ...rule,
    idx: index,
  })).sort((a, b) => (a.name === b.name ? a.idx - b.idx : a.name.localeCompare(b.name)));

  const ruleTypeNames = makeUniqueNames(
    sortedRules.map((rule) => rule.name),
    (name) => `CstRule_${sanitizeName(name, "Rule")}`,
  );
  const ruleValueNames = makeUniqueNames(
    sortedRules.map((rule) => rule.name),
    (name) => `cst_rule_${sanitizeName(name, "rule")}`,
  );

  const header = [
    "-- Auto-generated by scripts/syntax/gen-cst-from-ebnf.mjs",
    `-- source: ${sourceLabel}`,
    "",
    "module compiler.syntax_cst_generated",
    "import compiler.prelude",
    "export CstExpr, CstRule, cst_rules, cst_rule_count",
    "",
    "data CstExpr =",
    "  CstExprChoice (List CstExpr)",
    "  | CstExprSequence (List CstExpr)",
    "  | CstExprLiteral String",
    "  | CstExprReference String",
    "  | CstExprOptional CstExpr",
    "  | CstExprMany CstExpr",
    "  | CstExprOneOrMore CstExpr",
    "  | CstExprGroup CstExpr",
    "  | CstExprEmpty",
    "",
    "data CstRule = CstRule String CstExpr",
    "",
  ].join("\n");

  const ruleData = sortedRules.map((rule, ruleIndex) => {
    const ruleType = ruleTypeNames[ruleIndex];
    return `data ${ruleType} = ${ruleType} CstExpr`;
  }).join("\n");

  const ruleValues = sortedRules.map((rule, ruleIndex) => {
    const ruleType = ruleTypeNames[ruleIndex];
    const valueName = ruleValueNames[ruleIndex];
    return `${valueName} = CstRule "${escapeClapseString(rule.name)}" (${ruleType} ${exprToClapse(rule.expr)})`;
  }).join("\n");

  const listExpr = listToClapse(sortedRules.map((_, i) => ruleValueNames[i]));
  const rulesBlock = [
    "cst_rule_count = " + sortedRules.length,
    "",
    "cst_rules = " + (listExpr || "Nil"),
    "",
    ruleValues,
  ].join("\n");

  const ruleExports = sortedRules.map((_, i) => ruleValueNames[i]).join(", ");
  const allExports = ["CstExpr", "CstRule", "cst_rules", "cst_rule_count"];
  if (ruleExports.length > 0) {
    allExports.push(ruleExports);
  }
  const moduleExports = `export ${allExports.join(", ")}`;
  return header.replace("export CstExpr, CstRule, cst_rules, cst_rule_count", moduleExports) +
    ruleData + "\n\n" + rulesBlock + "\n";
}

function exprToClapse(expr) {
  switch (expr.kind) {
    case "choice": {
      const inner = expr.alts.map(exprToClapse);
      return `CstExprChoice (${listToClapse(inner)})`;
    }
    case "sequence": {
      const inner = expr.items.map(exprToClapse);
      return `CstExprSequence (${listToClapse(inner)})`;
    }
    case "literal":
      return `CstExprLiteral "${escapeClapseString(expr.value)}"`;
    case "reference":
      return `CstExprReference "${escapeClapseString(expr.name)}"`;
    case "optional":
      return `CstExprOptional (${exprToClapse(expr.expr)})`;
    case "many":
      return `CstExprMany (${exprToClapse(expr.expr)})`;
    case "oneOrMore":
      return `CstExprOneOrMore (${exprToClapse(expr.expr)})`;
    case "group":
      return `CstExprGroup (${exprToClapse(expr.expr)})`;
    case "empty":
    default:
      return "CstExprEmpty";
  }
}

function listToClapse(items) {
  return items.reduceRight((acc, item) => `Cons ${item} (${acc})`, "Nil");
}

function makeUniqueNames(names, formatter) {
  const seen = new Map();
  return names.map((name) => {
    const base = formatter(name);
    const count = seen.get(base) ?? 0;
    seen.set(base, count + 1);
    return count === 0 ? base : `${base}_${count}`;
  });
}

function sanitizeName(value, prefix) {
  const parts = value
    .split(/[^A-Za-z0-9]+/g)
    .filter(Boolean)
    .map((chunk, i) => {
      const lc = chunk.toLowerCase();
      return lc.charAt(0).toUpperCase() + lc.slice(1);
    });
  const name = (parts.length === 0 ? [prefix] : parts).join("");
  if (/^[A-Za-z_]/.test(name)) return name;
  return `${prefix}_${name}`;
}

class Lexer {
  constructor(text) {
    this.text = text;
    this.pos = 0;
  }

  tokenize() {
    const tokens = [];
    while (this.pos < this.text.length) {
      const ch = this.text[this.pos];
      if (isWhitespace(ch)) {
        this.pos += 1;
        continue;
      }
      if (ch === "#") {
        this.advanceToLineEnd();
        continue;
      }
      if (this.text.startsWith("--", this.pos)) {
        this.advanceToLineEnd();
        continue;
      }
      if (this.text.startsWith("(*", this.pos)) {
        this.advanceThrough("*)");
        continue;
      }
      if (ch === '"' || ch === "'" ) {
        tokens.push({
          type: "STRING",
          value: this.readQuotedString(),
        });
        continue;
      }
      if (/[A-Za-z_]/.test(ch)) {
        tokens.push({ type: "IDENT", value: this.readIdent() });
        continue;
      }
      const symbol = symbolFor(ch);
      if (symbol) {
        tokens.push({ type: symbol, value: ch });
        this.pos += 1;
        continue;
      }
      if (!isWhitespace(ch)) {
        this.pos += 1;
        tokens.push({ type: "STRING", value: ch });
        continue;
      }
      throw new SyntaxError(`Unexpected character at ${this.pos}: ${ch}`);
    }
    return tokens;
  }

  readIdent() {
    const start = this.pos;
    while (this.pos < this.text.length && /[A-Za-z0-9_-]/.test(this.text[this.pos])) {
      this.pos += 1;
    }
    return this.text.slice(start, this.pos);
  }

  readQuotedString() {
    const quote = this.text[this.pos];
    this.pos += 1;
    let out = "";
    while (this.pos < this.text.length) {
      const ch = this.text[this.pos];
      if (ch === "\\") {
        const next = this.text[this.pos + 1];
        this.pos += 1;
        if (next === undefined) {
          break;
        }
        if (next === "n") out += "\n";
        else if (next === "r") out += "\r";
        else if (next === "t") out += "\t";
        else if (next === "\\") out += "\\";
        else if (next === quote) out += quote;
        else out += next;
        this.pos += 1;
        continue;
      }
      if (ch === quote) {
        this.pos += 1;
        return out;
      }
      out += ch;
      this.pos += 1;
    }
    throw new SyntaxError("Unterminated string literal");
  }

  advanceToLineEnd() {
    while (this.pos < this.text.length && this.text[this.pos] !== "\n") {
      this.pos += 1;
    }
    if (this.pos < this.text.length && this.text[this.pos] === "\n") {
      this.pos += 1;
    }
  }

  advanceThrough(end) {
    const idx = this.text.indexOf(end, this.pos + 2);
    if (idx === -1) {
      this.pos = this.text.length;
      return;
    }
    this.pos = idx + end.length;
  }
}

function symbolFor(ch) {
  if (ch === "=") return "EQUALS";
  if (ch === ".") return "DOT";
  if (ch === "|") return "ALT";
  if (ch === "|") return "ALT";
  if (ch === "(") return "LPAREN";
  if (ch === ")") return "RPAREN";
  if (ch === "[") return "LBRACKET";
  if (ch === "]") return "RBRACKET";
  if (ch === "{") return "LBRACE";
  if (ch === "}") return "RBRACE";
  if (ch === "+") return "PLUS";
  if (ch === "*") return "STAR";
  if (ch === "?") return "QUESTION";
  if (ch === ";") return "SEMI";
  return null;
}

function isWhitespace(ch) {
  return ch === " " || ch === "\n" || ch === "\r" || ch === "\t" || ch === "\f";
}

class Parser {
  constructor(tokens) {
    this.tokens = tokens;
    this.pos = 0;
  }

  parseRules() {
    const rules = [];
    while (!this.eof()) {
      if (this.peek().type !== "IDENT") {
        throw this.error(`Expected rule name, found ${this.peek().type}`);
      }
      const name = this.consume("IDENT").value;
      this.consume("EQUALS");
      const expr = this.parseExpr();
      this.consumeOptional("DOT");
      this.consumeOptional("SEMI");
      rules.push({ name, expr });
    }
    return rules;
  }

  parseExpr() {
    const first = this.parseSequence();
    const alternatives = [first];
    while (this.consumeOptional("ALT")) {
      alternatives.push(this.parseSequence());
    }
    if (alternatives.length === 1) return alternatives[0];
    return { kind: "choice", alts: alternatives };
  }

  parseSequence() {
    const items = [];
    while (!this.eof()) {
      if (["ALT", "RPAREN", "RBRACKET", "RBRACE", "DOT", "SEMI"].includes(this.peek().type)) {
        break;
      }
      items.push(this.parseTerm());
    }
    if (items.length === 1) return items[0];
    if (items.length === 0) return { kind: "empty" };
    return { kind: "sequence", items };
  }

  parseTerm() {
    const atom = this.parseAtom();
    if (this.consumeOptional("PLUS")) {
      return { kind: "oneOrMore", expr: atom };
    }
    if (this.consumeOptional("STAR")) {
      return { kind: "many", expr: atom };
    }
    if (this.consumeOptional("QUESTION")) {
      return { kind: "optional", expr: atom };
    }
    return atom;
  }

  parseAtom() {
    const next = this.peek();
    if (this.consumeOptional("IDENT")) {
      return { kind: "reference", name: next.value };
    }
    if (this.consumeOptional("STRING")) {
      return { kind: "literal", value: next.value };
    }
    if (this.consumeOptional("LPAREN")) {
      const inner = this.parseExpr();
      this.consume("RPAREN");
      return { kind: "group", expr: inner };
    }
    if (this.consumeOptional("LBRACKET")) {
      const inner = this.parseExpr();
      this.consume("RBRACKET");
      return { kind: "optional", expr: inner };
    }
    if (this.consumeOptional("LBRACE")) {
      const inner = this.parseExpr();
      this.consume("RBRACE");
      return { kind: "many", expr: inner };
    }
    throw this.error(`Unexpected token in term: ${next.type}`);
  }

  consume(type) {
    if (!this.consumeOptional(type)) {
      throw this.error(`Expected ${type} token`);
    }
    return this.tokens[this.pos - 1];
  }

  consumeOptional(type) {
    if (this.eof()) {
      return false;
    }
    if (this.peek().type === type) {
      this.pos += 1;
      return true;
    }
    return false;
  }

  peek() {
    if (this.eof()) return { type: "EOF" };
    return this.tokens[this.pos];
  }

  eof() {
    return this.pos >= this.tokens.length;
  }

  error(message) {
    return new SyntaxError(`${message} at token ${this.pos}`);
  }
}
