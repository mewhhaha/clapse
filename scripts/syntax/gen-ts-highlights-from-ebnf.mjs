#!/usr/bin/env deno run

const DEFAULT_GRAMMAR_PATH = "docs/clapse-language/references/grammar.ebnf";
const DEFAULT_QUERY_PATH = "tree-sitter-clapse/queries/highlights.scm";
const BEGIN_MARKER = "; BEGIN GENERATED-HIGHLIGHTS FROM_EBNF";
const END_MARKER = "; END GENERATED-HIGHLIGHTS FROM_EBNF";

if (import.meta.main) {
  main().catch((error) => {
    console.error(error);
    Deno.exit(1);
  });
}

async function main() {
  const { mode, grammarPath, queryPath } = parseArgs(Deno.args);
  const [grammarText, queryText] = await Promise.all([
    Deno.readTextFile(grammarPath),
    Deno.readTextFile(queryPath),
  ]);
  const terminals = collectTerminals(grammarText);
  const managedBlock = emitManagedBlock(terminals, grammarPath);
  const nextQueryText = replaceManagedBlock(queryText, managedBlock, queryPath);

  if (mode === "check") {
    if (nextQueryText !== queryText) {
      throw new Error(
        `gen-ts-highlights-from-ebnf: managed highlights block is stale in ${queryPath}; run --write`,
      );
    }
    return;
  }

  await Deno.writeTextFile(queryPath, nextQueryText);
}

function parseArgs(args) {
  let mode = "write";
  const positional = [];
  for (const arg of args) {
    if (arg === "--check") {
      mode = "check";
      continue;
    }
    if (arg === "--write") {
      mode = "write";
      continue;
    }
    positional.push(arg);
  }
  return {
    mode,
    grammarPath: positional[0] ?? DEFAULT_GRAMMAR_PATH,
    queryPath: positional[1] ?? DEFAULT_QUERY_PATH,
  };
}

function collectTerminals(grammarText) {
  const textWithoutComments = grammarText.replace(/#.*$/gmu, "");
  const terminalValues = new Set();
  const pattern = /"([^"\\]*(?:\\.[^"\\]*)*)"/gmu;
  let match = pattern.exec(textWithoutComments);
  while (match !== null) {
    const token = decodeEscapes(match[1]);
    if (shouldIncludeToken(token)) {
      terminalValues.add(token);
    }
    match = pattern.exec(textWithoutComments);
  }
  return classify([...terminalValues]);
}

function decodeEscapes(raw) {
  try {
    return JSON.parse(`"${raw}"`);
  } catch {
    return raw;
  }
}

function shouldIncludeToken(token) {
  if (token.length === 0) return false;
  if (/\s/u.test(token)) return false;
  if (token === "'" || token === '"' || token === "_") return false;
  return true;
}

function classify(tokens) {
  const keyword = [];
  const operator = [];
  const punctuationBracket = [];
  const punctuationDelimiter = [];
  const bracketTokens = new Set(["(", ")", "{", "}", "[", "]"]);
  for (const token of tokens) {
    if (/^[A-Za-z_][A-Za-z0-9_]*$/u.test(token)) {
      keyword.push(token);
      continue;
    }
    if (token === ",") {
      punctuationDelimiter.push(token);
      continue;
    }
    if (bracketTokens.has(token)) {
      punctuationBracket.push(token);
      continue;
    }
    operator.push(token);
  }
  const sorter = (a, b) => {
    if (a < b) return -1;
    if (a > b) return 1;
    return 0;
  };
  keyword.sort(sorter);
  operator.sort(sorter);
  punctuationBracket.sort(sorter);
  punctuationDelimiter.sort(sorter);
  return {
    keyword,
    operator,
    punctuationBracket,
    punctuationDelimiter,
  };
}

function emitManagedBlock(terminals, grammarPath) {
  const lines = [
    BEGIN_MARKER,
    `; source: ${grammarPath}`,
    "; generated by scripts/syntax/gen-ts-highlights-from-ebnf.mjs",
    "",
  ];
  emitTokenSet(lines, terminals.punctuationBracket, "@punctuation.bracket");
  emitTokenSet(lines, terminals.punctuationDelimiter, "@punctuation.delimiter");
  emitTokenSet(lines, terminals.operator, "@keyword.operator");
  emitTokenSet(lines, terminals.keyword, "@keyword");
  if (lines[lines.length - 1] === "") {
    lines.pop();
  }
  lines.push(END_MARKER);
  return lines.join("\n");
}

function emitTokenSet(lines, tokens, captureName) {
  if (tokens.length === 0) {
    return;
  }
  lines.push("[");
  for (const token of tokens) {
    lines.push(`  "${escapeQueryString(token)}"`);
  }
  lines.push(`] ${captureName}`, "");
}

function escapeQueryString(text) {
  return text.replaceAll("\\", "\\\\").replaceAll('"', '\\"');
}

function replaceManagedBlock(queryText, blockText, queryPath) {
  const start = queryText.indexOf(BEGIN_MARKER);
  const end = queryText.indexOf(END_MARKER);
  if (start < 0 || end < 0 || end < start) {
    throw new Error(
      `gen-ts-highlights-from-ebnf: managed block markers missing in ${queryPath}`,
    );
  }
  const blockEnd = end + END_MARKER.length;
  return queryText.slice(0, start) + blockText + queryText.slice(blockEnd);
}
